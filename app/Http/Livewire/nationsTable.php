        return [];
    }

    /*
    |--------------------------------------------------------------------------
    |  Add Column
    |--------------------------------------------------------------------------
    | Make Datasource fields available to be used as columns.
    | You can pass a closure to transform/modify the data.
    |
    | ❗ IMPORTANT: When using closures, you must escape any value coming from
    |    the database using the `e()` Laravel Helper function.
    |
    */
    public function addColumns(): PowerGridColumns
    {
        return PowerGrid::columns()
            ->addColumn('id')
            ->addColumn('name')

           /** Example of custom column using a closure **/
            ->addColumn('name_lower', fn (Nation $model) => strtolower(e($model->name)))

            ->addColumn('flag')
            ->addColumn('enable_send')
            ->addColumn('enable_receive')
            ->addColumn('flagpng')
            ->addColumn('created_at_formatted', fn (Nation $model) => Carbon::parse($model->created_at)->format('d/m/Y H:i:s'));
    }

    /*
    |--------------------------------------------------------------------------
    |  Include Columns
    |--------------------------------------------------------------------------
    | Include the columns added columns, making them visible on the Table.
    | Each column can be configured with properties, filters, actions...
    |
    */

     /**
      * PowerGrid Columns.
      *
      * @return array<int, Column>
      */
    public function columns(): array
    {
        return [
            Column::make('Id', 'id'),
            Column::make('Name', 'name')
                ->sortable()
                ->searchable(),

            Column::make('Flag', 'flag')
                ->sortable()
                ->searchable(),

            Column::make('Enable send', 'enable_send')
                ->toggleable(),

            Column::make('Enable receive', 'enable_receive')
                ->toggleable(),

            Column::make('Flagpng', 'flagpng')
                ->sortable()
                ->searchable(),

            Column::make('Created at', 'created_at_formatted', 'created_at')
                ->sortable(),

        ];
    }

    /**
     * PowerGrid Filters.
     *
     * @return array<int, Filter>
     */
    public function filters(): array
    {
        return [
            Filter::inputText('name')->operators(['contains']),
            Filter::inputText('flag')->operators(['contains']),
            Filter::boolean('enable_send'),
            Filter::boolean('enable_receive'),
            Filter::datetimepicker('created_at'),
        ];
    }

